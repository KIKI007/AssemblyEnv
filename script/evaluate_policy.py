import itertools
def evaluate(queue):
    parts = queue.get()
    assembly = Assembly2D(parts)
    env = RobotPlayground(assembly)
    env.render = False
    env.send_time_delay = 1
    model = PPO.load(f"models/PPO_2/{4}", env)

    num_total_states = 0
    num_sucess_states = 0
    for part_state in itertools.product([0, 1], repeat=assembly.n_part() - 1):
        env.set_install_state(part_state)
        if assembly.check_stability(env.current_label()) != None and env.n_installed() < env.n_part():
            num_total_states = num_total_states + 1
            obs = env._get_obs()
            while True:
                action, _state = model.predict(obs, deterministic=True)
                obs, reward, terminated, truncated, info = env.step(action)
                if terminated or truncated:
                    if reward > 0:
                        num_sucess_states += 1
                    else:
                        print(part_state)
                    break

    print(f"{num_sucess_states}/{num_total_states}")

